public class Rozmery {
    private int rozmerA;
    private int rozmerB;
    private int rozmerC;

    public Rozmery(int rozmerA, int rozmerB, int rozmerC) {
        this.rozmerA = rozmerA;
        this.rozmerB = rozmerB;
        this.rozmerC = rozmerC;
    }

    public int getRozmerA() {
        return rozmerA;
    }

    public void setRozmerA(int rozmerA) {
        this.rozmerA = rozmerA;
    }

    public int getRozmerB() {
        return rozmerB;
    }

    public void setRozmerB(int rozmerB) {
        this.rozmerB = rozmerB;
    }

    public int getRozmerC() {
        return rozmerC;
    }

    public void setRozmerC(int rozmerC) {
        this.rozmerC = rozmerC;
    }
}
